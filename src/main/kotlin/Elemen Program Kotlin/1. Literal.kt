package `Elemen Program Kotlin`

fun main(args: Array<String>) {
    var intLiteral = 5 // Literal dengan Tipe Data Integer
    var doubleLiteral = .02 //Literal dengan Tipe Data Double, Float(
    var stringLiteral = "Hello" //Literal dengan Tipe Data String
    var charLiteral = 'A' //Literal dengan Tipe Data Character atau Char
    var boolLiteral = true //Literal dengan Tipe Data Boolean
    // Mencetak string dan variable dari masing-masing Tipe Data Literal
    println("intLiteral = " + intLiteral)
    println("doubleLiteral = " + doubleLiteral)
    println("stringLiteral = " + stringLiteral)
    println("charLiteral = " + charLiteral)
    println("boolLiteral = " + boolLiteral)

}